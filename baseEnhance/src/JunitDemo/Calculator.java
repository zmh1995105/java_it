package JunitDemo;

public class Calculator {

    /**
     * add
     * @param a
     * @param b
     * @return
     */
    public int add (int a, int b) {
        return a + b;
    }

    /**
     * sub
     * @param a
     * @param b
     * @return
     */
    public int sub (int a, int b) {
        return a - b;
    }
}
